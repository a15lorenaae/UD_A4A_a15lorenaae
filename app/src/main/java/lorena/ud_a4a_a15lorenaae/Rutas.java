public class Rutas {
    private String nome;
    private String descripcion;

    public Rutas() {
        this.nome = nome;
        this.descripcion = descripcion;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
}
